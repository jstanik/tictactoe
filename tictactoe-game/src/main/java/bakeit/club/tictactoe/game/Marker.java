package bakeit.club.tictactoe.game;

/**
 * A marker symbol that players can put on a board.
 */
public enum Marker {

  /**
   * The 'X' marker one of the players uses to make his moves.
   */
  X,

  /**
   * The 'O' marker one of the players uses to make his moves.
   */
  O,
  /**
   * Empty marker means there is value at the given cell.
   */
  EMPTY,
}
